import tensorflow as tf
from tensorflow.keras import layers, Model

# ---------------------------------------------------
# Identity Block
# ---------------------------------------------------
def identity_block(x, filters, kernel_size=3):
    f1, f2, f3 = filters
    shortcut = x

    x = layers.Conv2D(f1, (1, 1), strides=(1, 1), padding='valid')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation('relu')(x)

    x = layers.Conv2D(f2, (kernel_size, kernel_size), strides=(1, 1), padding='same')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation('relu')(x)

    x = layers.Conv2D(f3, (1, 1), strides=(1, 1), padding='valid')(x)
    x = layers.BatchNormalization()(x)

    x = layers.Add()([x, shortcut])
    x = layers.Activation('relu')(x)

    return x


# ---------------------------------------------------
# Convolutional Block
# Used when dimensions need to change
# ---------------------------------------------------
def conv_block(x, filters, stride=2, kernel_size=3):
    f1, f2, f3 = filters
    shortcut = x

    x = layers.Conv2D(f1, (1, 1), strides=(stride, stride), padding='valid')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation('relu')(x)

    x = layers.Conv2D(f2, (kernel_size, kernel_size), strides=(1, 1), padding='same')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation('relu')(x)

    x = layers.Conv2D(f3, (1, 1), strides=(1, 1), padding='valid')(x)
    x = layers.BatchNormalization()(x)

    shortcut = layers.Conv2D(f3, (1, 1), strides=(stride, stride), padding='valid')(shortcut)
    shortcut = layers.BatchNormalization()(shortcut)

    x = layers.Add()([x, shortcut])
    x = layers.Activation('relu')(x)

    return x


# ---------------------------------------------------
# ResNet-152 Model
# Block distribution = [3, 8, 36, 3]
# ---------------------------------------------------
def ResNet152(input_shape=(224, 224, 3), classes=1000):
    inputs = layers.Input(shape=input_shape)

    # Initial layers
    x = layers.ZeroPadding2D((3, 3))(inputs)
    x = layers.Conv2D(64, (7, 7), strides=(2, 2), padding='valid')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation('relu')(x)
    x = layers.ZeroPadding2D((1, 1))(x)
    x = layers.MaxPooling2D((3, 3), strides=(2, 2))(x)

    # Stage 2 -> 3 blocks
    x = conv_block(x, [64, 64, 256], stride=1)
    for _ in range(2):
        x = identity_block(x, [64, 64, 256])

    # Stage 3 -> 8 blocks
    x = conv_block(x, [128, 128, 512], stride=2)
    for _ in range(7):
        x = identity_block(x, [128, 128, 512])

    # Stage 4 -> 36 blocks
    x = conv_block(x, [256, 256, 1024], stride=2)
    for _ in range(35):
        x = identity_block(x, [256, 256, 1024])

    # Stage 5 -> 3 blocks
    x = conv_block(x, [512, 512, 2048], stride=2)
    for _ in range(2):
        x = identity_block(x, [512, 512, 2048])

    # Final layers
    x = layers.GlobalAveragePooling2D()(x)
    outputs = layers.Dense(classes, activation='softmax')(x)

    model = Model(inputs, outputs, name='ResNet152')
    return model


