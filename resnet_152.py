        img_np = np.array([0.229, 0.224, 0.225]) * img_np + np.array([0.485, 0.456, 0.406])
        img_np = np.clip(img_np, 0, 1)

        ax = axes[i // 5, i % 5]
        ax.imshow(img_np)
        ax.set_title(f"True: {cifar10_classes[true_label.item()]}\nPred: {imagenet_classes[predicted.item()]}")
        ax.axis("off")

